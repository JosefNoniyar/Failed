// भुगतान असफलता के संभावित कारण
const failureReasons = [
    "आपके कार्ड में पर्याप्त शेष राशि नहीं है।",
    "भुगतान गेटवे ने अनुरोध अस्वीकार कर दिया।",
    "नेटवर्क समस्या के कारण भुगतान पूरा नहीं हो सका।",
    "आपके बैंक ने लेन-देन को रोक दिया।",
    "सुरक्षा कारणों से भुगतान रोका गया।",
    "अमान्य भुगतान विवरण दर्ज किए गए।"
];

// रैंडम कारण चुनें और दिखाएँ
document.getElementById("failure-message").innerText = 
    failureReasons[Math.floor(Math.random() * failureReasons.length)];

// पुनः प्रयास करने की क्रिया
function retryPayment() {
    alert("कृपया पहले प्रोग्राम में वापस जाएं और पुनः भुगतान करें।\n\n➡ टर्मिनल में वापस जाने के लिए: terminal://redirect");
}

// भुगतान रद्द करने की क्रिया
function cancelPayment() {
    alert("धन्यवाद! यदि आपको सहायता की आवश्यकता हो, तो कृपया संपर्क करें।");
}